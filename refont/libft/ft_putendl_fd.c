#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	a;

	if (s)
	{
		i = 0;
		a = '\n';
		while (s[i] != '\0')
			write (fd, &s[i++], 1);
		write (fd, &a, 1);
	}
}
