#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s)
	{
		i = 0;
		while (s[i] != '\0')
			write(fd, &s[i++], 1);
	}
	return (i);
}
